# =========================================================
# 🔍 CATEGORÍA DOMINANTE
# =========================================================

def get_dominant_category(category_scores):

    return max(
        category_scores,
        key=category_scores.get
    )

def get_emotion_level(category_scores):

    ansiedad = category_scores.get("ansiedad", 0)
    estres = category_scores.get("estres", 0)
    depresion = category_scores.get("depresion", 0)
    bienestar = category_scores.get("bienestar", 0)

    critical_count = 0

    if ansiedad >= 80:
        critical_count += 1

    if estres >= 80:
        critical_count += 1

    if depresion >= 80:
        critical_count += 1

    # 🔵 CRÍTICO
    if critical_count >= 3:
        return "Azul"

    # 🟣 DEPRESIÓN
    if depresion >= ansiedad and depresion >= estres:
        return "Morado"

    # 🟠 ESTRÉS
    if estres >= ansiedad:
        return "Naranja"

    # 🟡 ANSIEDAD
    if ansiedad > estres:
        return "Amarillo"

    # 🟢 BIENESTAR
    return "Verde"


# =========================================================
# 📈 SEVERIDAD EMOCIONAL
# =========================================================

def get_severity(level, category_scores):

    ansiedad = category_scores.get("ansiedad", 0)
    estres = category_scores.get("estres", 0)
    depresion = category_scores.get("depresion", 0)
    bienestar = category_scores.get("bienestar", 0)

    negative_load = ansiedad + estres + depresion

    severity_score = negative_load - bienestar

    if level == "Azul" or severity_score >= 180:
        return "critical"

    elif severity_score >= 120:
        return "high"

    elif severity_score >= 70:
        return "moderate"

    return "standard"


# =========================================================
# 🧠 CONSTRUIR PLAN DE INTERVENCIÓN
# =========================================================

def build_intervention_plan(level, category_scores):

    emotion_level = get_emotion_level(category_scores)

    base_plan = INTERVENTION_LIBRARY.get(
        emotion_level,
        {}
    )

    plan = []

    for section, items in base_plan.items():

        plan.append({
            "section": section,
            "items": items
        })

    return plan